    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        try {
            if (txtIDSupplier.getText().isEmpty()||
                txtALamat.getText().isEmpty()||
                txtKota.getText().isEmpty()||
                txtNamaSupplier.getText().isEmpty()||
                txtTelepon.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Data Tidak Boleh Kosong, Silahkan Lengkapu");
            }else{
            Class.forName(driver);
            kon = DriverManager.getConnection(database, user, pass);
            stt = kon.createStatement();
            
            String data[] = new String[5];

            data[0] = txtIDSupplier.getText();
            data[1] = txtNamaSupplier.getText();
            data[2] = txtALamat.getText();
            data[3] = txtKota.getText();
            data[4] = txtTelepon.getText();

            sql = "UPDATE `10115416_apotekdb`.`t_supplier` SET"
                    + " `Id_Supplier` = '" + data[0] + "' ,"
                    + " `Nama_Supplier` = '" + data[1] + "' ,"
                    + " `Alamat` = '" + data[2] + "' ,"
                    + " `Kota` = '" + data[3] + "' ,"
                    + " `No_Telepon` = '" + data[4] + "' "                                                  
                    + "WHERE `Id_Supplier` = '" + IDSupplier + "'";                        
            stt.execute(sql);
            
            kon.close();
            stt.close();
            
            dataSupplier.tableModel.removeRow(row);
            dataSupplier.tableModel.insertRow(row, data);

            JOptionPane.showMessageDialog(this, "Sukses mengubah data");
            this.dispose();
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnUbahActionPerformed

